// define a new data type, Enemy_State, for the  enemy sprites
struct Enemy_State {
  unsigned long x;      // x coordinate
  unsigned long y;      // y coordinate
  const unsigned char *image[2]; // two pointers to images
  long life;            // 0=dead, 1=alive
};
